/* instruction fetch */
/* 10 Points */
int instruction_fetch(unsigned PC,unsigned *Mem,unsigned *instruction)
{
    if (((PC >> 2) + 31) > (65536 >> 2)) // Checking if new PC goes out of bounds
        return 1;

    else {
        instruction = &(Mem[PC >> 2]); // instruction now points to next instruction word
        return 0;
    }
    
    
}


